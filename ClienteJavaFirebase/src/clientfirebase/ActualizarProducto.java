    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        String user=jTextField7.getText();
        String pass=new String(jPasswordField1.getPassword());
        String isbn,autor,editorial,nombre;
        float precio;
        int agno;
        isbn=jTextField1.getText();
        nombre=jTextField2.getText();
        autor=jTextField3.getText();
        editorial=jTextField6.getText();
        precio=Float.parseFloat(jTextField5.getText());
        agno=Integer.parseInt(jTextField4.getText());
        
        Producto productoAct=new Producto(isbn,autor,nombre,editorial,agno,precio);
        Gson gson=new Gson();
        Gson gson1=new Gson();
        String clave=gson1.toJson(isbn);
        String jsonProd="{"+clave+":"+gson.toJson(productoAct)+"}";
        System.out.println(jsonProd);
        OkHttpClient client = new OkHttpClient().newBuilder().build();
        MediaType mediaType = MediaType.parse("text/plain");
        RequestBody body = RequestBody.create(mediaType, "");
        Request request = new Request.Builder()
            .url("http://pf.test:82/updateProd/"+user+"&"+pass+"&"+jsonProd).method("PUT", body).build();
        try {
            Response response = client.newCall(request).execute();
            if (response.code() != 200) {
                throw new RuntimeException("Failed : HTTP Error code : "+ response.code());
            }
            
            @SuppressWarnings("null")
            String cuerpoResp=response.body().string();
            System.out.println(cuerpoResp);
            Gson gsonRespuesta = new Gson();
            Respuesta respuestaServicio = gsonRespuesta.fromJson(cuerpoResp,Respuesta.class);
            System.out.println("\nCode: "+respuestaServicio.getCode()+"\nMessage: "+respuestaServicio.getMessage()+
                    "\nData: "+respuestaServicio.getData());
            String texto="Estatus "+respuestaServicio.getStatus()+" : "+respuestaServicio.getMessage()
                    +"(Código "+respuestaServicio.getCode()+").";
            jLabel7.setText(texto);
        } catch (IOException ex) {
            Logger.getLogger(VerTodos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2MouseClicked
    
